FITDECK_TEMPLATE = r"""* ============================================================
* Fitted bipolar memristive-device model
* Generated by fit_memristor_pso.py
* ============================================================

Vcmd vc 0 PWL(
@PWL_INLINE@
+ )

Vsen vc p0 0

.param ICOMP = @ICOMP_POS@
.param VCOMP = @VCOMP@
.param RLO = @RLO@
.param RHI = @RHI@
.param ISLOPE = @ISLOPE@
.param VSLOPE_POS = @VSLOPE_POS@

.func smooth_step(u) { 0.5*(1+tanh(u)) }
.func positive_gate(v) { smooth_step((v-VCOMP)/VSLOPE_POS) }
.func current_gate(i) { smooth_step((abs(i)-ICOMP)/ISLOPE) }

Rlim p0 p r={RLO+(RHI-RLO)*positive_gate(V(vc))*current_gate(i(Vsen))}
X1 p 0 x xh memdiode_bipolar CH0=@CH0@ H0=@H0@

.options method=gear reltol=4e-3 abstol=2e-15 vntol=2e-9
+ chgtol=2e-16 gmin=1e-12 itl1=900 itl4=350

.control
  set noaskquit
  set filetype=ascii
  set wr_singlescale
  tran @TSTEP@ @TSTOP@ 0 @DTMAX@ uic
  wrdata @SIMOUT@ v(vc) v(p) i(Vcmd) v(x) v(xh)
  quit
.endc

.subckt memdiode_bipolar p n x xh CH0=@CH0@ H0=@H0@

.param RH0=@RH0@
.param RH_MIN=@RH_MIN@
.param RH_MAX=@RH_MAX@
.param VSLOPE=@VSLOPE@

.param IMAX=@IMAX@
.param IMIN=@IMIN@
.param ALPHAMX=@ALPHA_MAX@
.param ALPHAMN=@ALPHA_MIN@
.param BETA=@BETAA@
.param VSET=@VSET@
.param VRES=@VRES@
.param ETA_SET=@ETA_SET@
.param ETA_RES=@ETA_RES@
.param ISCALE=@ISCALE@
.param EI=@EI@
.param ROFF=@ROFF@
.param CPAR=1p

.func softclip(v) {0.5*(tanh(4*(v-0.5))+1)}
.func blend(w,a,b) {w*a+(1-w)*b}
.func safe_exp(v) {exp(min(max(v,-25),25))}
.func sstep(v) {0.5*(1+tanh(v/VSLOPE))}
.func I0(h) {IMAX*h+IMIN*(1-h)}
.func Acoef(h) {ALPHAMX*h+ALPHAMN*(1-h)}
.func Stau(v) {RH0*safe_exp(-ETA_SET*(v-VSET))}
.func Rtau(v) {RH0*safe_exp(ETA_RES*(v+VRES))}
.func Idiff(v,h) {safe_exp(BETA*Acoef(h)*v)-safe_exp(-(1-BETA)*Acoef(h)*v)}

Bxh xh 0 V={softclip(V(x))}
Rs p d 1
BA A 0 V={sstep(V(d,n))}
Rh x A r={min(RH_MAX,max(RH_MIN,blend(V(A),Stau(V(d,n)),Rtau(V(d,n)))))}
Cx x 0 {CH0} ic={H0}
Rxl x 0 1e12
Bcond d n I={ISCALE*(I0(V(xh))*Idiff(V(d,n),V(xh))+EI*V(d,n))}
Rleak d n {ROFF}
Cpar d 0 {CPAR}

.ends memdiode_bipolar
.end
"""


