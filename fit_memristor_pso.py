def ensure_directories() -> None:
    for directory in (OUT_DIR, LOG_DIR, DECK_DIR, SIM_DIR):
        directory.mkdir(parents=True, exist_ok=True)
    TEMPLATE_PATH.write_text(FITDECK_TEMPLATE, encoding="utf-8")


def select_measurement_path() -> Path:
    if PRIVATE_DATA.exists():
        return PRIVATE_DATA
    if SYNTHETIC_DATA.exists():
        print(
            "[WARN] data/DC-IV.csv was not found; using the public synthetic "
            "example instead."
        )
        return SYNTHETIC_DATA
    raise FileNotFoundError(
        "Missing data/DC-IV.csv. A synthetic fallback may be stored as "
        "data/example_synthetic.csv."
    )


def read_measurement(path: Path) -> tuple[np.ndarray, np.ndarray]:
    table = pd.read_csv(path, engine="python")
    if table.shape[1] < 2:
        raise ValueError("The measurement CSV must contain at least two columns.")

    voltage = pd.to_numeric(table.iloc[:, 0], errors="coerce")
    current = pd.to_numeric(table.iloc[:, 1], errors="coerce")
    valid = voltage.notna() & current.notna()

    v = voltage[valid].to_numpy(dtype=float)
    i = current[valid].to_numpy(dtype=float)
    if len(v) < 20:
        raise ValueError("Too few numeric measurement rows remain after cleaning.")
    return v, i


def estimate_positive_compliance(voltage: np.ndarray, current: np.ndarray) -> float:
    mask = voltage > 0.5
    if not np.any(mask):
        return 1e-3
    estimate = float(np.quantile(np.abs(current[mask]), 0.98))
    return float(np.clip(estimate, 1e-6, 5e-2))


def pick_print_step(point_count: int, tstop: float) -> float:
    dt_command = tstop / max(point_count - 1, 1)
    return float(np.clip(dt_command / PRINT_DIV, PRINT_MIN, PRINT_MAX))


def make_pwl(voltage: np.ndarray, tstop: float, pairs_per_line: int = 8) -> str:
    time = np.linspace(0.0, tstop, len(voltage))
    pairs = [f"{t:.12g} {v:.12g}" for t, v in zip(time, voltage)]
    return "\n".join(
        "+ " + " ".join(pairs[start : start + pairs_per_line])
        for start in range(0, len(pairs), pairs_per_line)
    )


def safe_tag(text: str) -> str:
    text = re.sub(r"[^A-Za-z0-9_.-]+", "_", str(text))
    return re.sub(r"_+", "_", text).strip("_")


# =============================================================================
# Parameter encoding
# =============================================================================


def encode_parameters(theta: dict[str, float]) -> np.ndarray:
    encoded = []
    for name in PARAMETERS:
        value = float(theta[name])
        encoded.append(math.log10(value) if name in LOG_PARAMETERS else value)
    return np.asarray(encoded, dtype=float)


def decode_parameters(position: np.ndarray) -> dict[str, float]:
    theta: dict[str, float] = {}
    for index, name in enumerate(PARAMETERS):
        value = float(position[index])
        theta[name] = 10.0**value if name in LOG_PARAMETERS else value
    return clip_theta(theta)


def encoded_bounds() -> tuple[np.ndarray, np.ndarray]:
    lower = []
    upper = []
    for name in PARAMETERS:
        lo, hi = BOUNDS[name]
        if name in LOG_PARAMETERS:
            lower.append(math.log10(lo))
            upper.append(math.log10(hi))
        else:
            lower.append(lo)
            upper.append(hi)
    return np.asarray(lower, dtype=float), np.asarray(upper, dtype=float)


def clip_theta(theta: dict[str, float]) -> dict[str, float]:
    return {
        name: float(np.clip(theta[name], BOUNDS[name][0], BOUNDS[name][1]))
        for name in PARAMETERS
    }


# =============================================================================
# NGSpice simulation
# =============================================================================


def render_deck(
    theta: dict[str, float],
    voltage: np.ndarray,
    sim_path: Path,
    icomp: float,
) -> str:
    tstep = pick_print_step(len(voltage), TSTOP)
    islope = max(1e-12, COMPLIANCE_SETTINGS["ISLOPE_REL"] * icomp)

    replacements: dict[str, str] = {
        "@PWL_INLINE@": make_pwl(voltage, TSTOP),
        "@SIMOUT@": str(sim_path),
        "@TSTEP@": f"{tstep:.12g}",
        "@DTMAX@": f"{DTMAX:.12g}",
        "@TSTOP@": f"{TSTOP:.12g}",
        "@ICOMP_POS@": f"{icomp:.12g}",
        "@VCOMP@": f"{COMPLIANCE_SETTINGS['VCOMP']:.12g}",
        "@RLO@": f"{COMPLIANCE_SETTINGS['RLO']:.12g}",
        "@RHI@": f"{COMPLIANCE_SETTINGS['RHI']:.12g}",
        "@ISLOPE@": f"{islope:.12g}",
        "@VSLOPE_POS@": f"{COMPLIANCE_SETTINGS['VSLOPE_POS']:.12g}",
    }

    for name, value in FIXED_KNOBS.items():
        replacements[f"@{name}@"] = f"{value:.12g}"
    for name, value in theta.items():
        replacements[f"@{name}@"] = f"{value:.12g}"

    text = FITDECK_TEMPLATE
    for token, replacement in replacements.items():
        text = text.replace(token, replacement)

    leftovers = sorted(set(re.findall(r"@[A-Za-z0-9_]+@", text)))
    if leftovers:
        raise RuntimeError(f"Unreplaced SPICE placeholders: {leftovers}")
    return text


def run_simulation(
    theta: dict[str, float],
    voltage: np.ndarray,
    icomp: float,
    tag: str,
) -> SimulationResult | None:
    tag = safe_tag(tag)
    deck_path = DECK_DIR / f"{tag}.cir"
    log_path = LOG_DIR / f"{tag}.log"
    sim_path = SIM_DIR / f"{tag}.dat"

    deck_path.write_text(
        render_deck(theta, voltage, sim_path, icomp),
        encoding="utf-8",
    )

    try:
        completed = subprocess.run(
            [str(NGSPICE), "-b", "-o", str(log_path), str(deck_path)],
            cwd=str(OUT_DIR),
            timeout=NGSPICE_TIMEOUT_S,
            check=False,
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
        )
    except (OSError, subprocess.TimeoutExpired):
        return None

    if completed.returncode != 0 or not sim_path.exists():
        return None

    try:
        data = np.loadtxt(sim_path, dtype=float)
    except (OSError, ValueError):
        return None

    if data.ndim == 1:
        data = data.reshape(1, -1)
    if data.shape[1] < 6 or len(data) < 8:
        return None

    return SimulationResult(
        time=data[:, 0],
        vcmd=data[:, 1],
        vp=data[:, 2],
        current=data[:, 3],
        x=data[:, 4],
        xh=data[:, 5],
    )


# =============================================================================
# Objective function
# =============================================================================


def build_region_weights(voltage: np.ndarray, current: np.ndarray) -> np.ndarray:
    abs_v = np.abs(voltage)
    abs_i = np.abs(current)
    weights = np.ones_like(voltage, dtype=float)

    # Near-zero behavior and loop closure.
    weights[abs_v <= 0.25] *= 1.8

    # Positive SET region and positive high-resistance branch.
    weights[(voltage >= 0.5) & (voltage <= 4.0)] *= 1.4
    weights[voltage >= 4.0] *= 1.25

    # Negative RESET branch receives extra emphasis because its current can be
    # much smaller than the positive branch in absolute units.
    weights[voltage < -0.5] *= 1.8
    weights[voltage < -3.0] *= 1.35

    # Emphasize the measured switching knee without letting isolated points
    # dominate the objective.
    if np.any(abs_i > I_FLOOR):
        log_i = np.log10(abs_i + I_FLOOR)
        gradient = np.abs(np.gradient(log_i))
        threshold = np.quantile(gradient, 0.88)
        weights[gradient >= threshold] *= 1.5

    return weights / np.mean(weights)


def aligned_simulation_current(
    result: SimulationResult,
    target_count: int,
) -> np.ndarray:
    phase = result.time / max(float(result.time[-1]), 1e-30)
    target_phase = np.linspace(0.0, 1.0, target_count)
    return np.interp(target_phase, phase, result.current)


def calculate_cost(
    measured_current: np.ndarray,
    simulated_current: np.ndarray,
    weights: np.ndarray,
    xh: np.ndarray,
) -> float:
    measured_log = np.log10(np.abs(measured_current) + I_FLOOR)
    simulated_log = np.log10(np.abs(simulated_current) + I_FLOOR)

    log_error = np.average((simulated_log - measured_log) ** 2, weights=weights)

    measured_scale = np.quantile(np.abs(measured_current), 0.95) + I_FLOOR
    signed_error = np.average(
        ((simulated_current - measured_current) / measured_scale) ** 2,
        weights=weights,
    )

    # Encourage a meaningful internal-state span and bounded effective state.
    xh_span = float(np.max(xh) - np.min(xh))
    state_penalty = max(0.0, 0.12 - xh_span) ** 2 * 20.0
    bound_penalty = float(np.mean(np.maximum(0.0, -xh) ** 2 + np.maximum(0.0, xh - 1.0) ** 2))

    total = float(log_error + 0.15 * signed_error + state_penalty + 10.0 * bound_penalty)
    return total if np.isfinite(total) else FAIL_COST


def make_objective(
    voltage: np.ndarray,
    current: np.ndarray,
    icomp: float,
    stage: str,
) -> Callable[[dict[str, float], int], float]:
    weights = build_region_weights(voltage, current)

    def objective(theta: dict[str, float], evaluation_index: int) -> float:
        result = run_simulation(
            theta,
            voltage,
            icomp,
            tag=f"{stage}_{evaluation_index:07d}",
        )
        if result is None:
            return FAIL_COST

        simulated = aligned_simulation_current(result, len(current))
        return calculate_cost(current, simulated, weights, result.xh)

    return objective


# =============================================================================
# Particle swarm optimization
# =============================================================================


def latin_hypercube_positions(
    rng: np.random.Generator,
    count: int,
    lower: np.ndarray,
    upper: np.ndarray,
) -> np.ndarray:
    dimensions = len(lower)
    positions = np.empty((count, dimensions), dtype=float)
    for dimension in range(dimensions):
        points = (np.arange(count) + rng.random(count)) / count
        rng.shuffle(points)
        positions[:, dimension] = lower[dimension] + points * (
            upper[dimension] - lower[dimension]
        )
    return positions


def run_pso(
    *,
    objective: Callable[[dict[str, float], int], float],
    particle_count: int,
    iteration_count: int,
    rng: np.random.Generator,
    initial_center: dict[str, float] | None = None,
) -> tuple[dict[str, float], float, list[dict[str, float]]]:
    lower, upper = encoded_bounds()
    dimensions = len(lower)

    if initial_center is None:
        positions = latin_hypercube_positions(rng, particle_count, lower, upper)
    else:
        center = encode_parameters(initial_center)
        positions = np.tile(center, (particle_count, 1))
        for index, name in enumerate(PARAMETERS):
            if name in LOG_PARAMETERS:
                positions[:, index] += rng.normal(
                    0.0, REFINE_LOG_JITTER_DECADES, particle_count
                )
            else:
                width = upper[index] - lower[index]
                positions[:, index] += rng.normal(
                    0.0, REFINE_LINEAR_JITTER * width, particle_count
                )
        positions[0] = center
        positions = np.clip(positions, lower, upper)

    velocity_scale = 0.08 * (upper - lower)
    velocities = rng.normal(0.0, velocity_scale, size=(particle_count, dimensions))

    personal_positions = positions.copy()
    personal_costs = np.full(particle_count, np.inf)
    global_position = positions[0].copy()
    global_cost = np.inf
    history: list[dict[str, float]] = []
    evaluation_index = 0

    for iteration in range(iteration_count):
        for particle in range(particle_count):
            evaluation_index += 1
            theta = decode_parameters(positions[particle])
            cost = objective(theta, evaluation_index)

            if cost < personal_costs[particle]:
                personal_costs[particle] = cost
                personal_positions[particle] = positions[particle].copy()
            if cost < global_cost:
                global_cost = cost
                global_position = positions[particle].copy()

        progress = iteration / max(iteration_count - 1, 1)
        inertia = INERTIA_MAX + progress * (INERTIA_MIN - INERTIA_MAX)

        r1 = rng.random((particle_count, dimensions))
        r2 = rng.random((particle_count, dimensions))
        velocities = (
            inertia * velocities
            + COGNITIVE_COEFF * r1 * (personal_positions - positions)
            + SOCIAL_COEFF * r2 * (global_position - positions)
        )
        positions = np.clip(positions + velocities, lower, upper)

        history.append(
            {
                "iteration": float(iteration + 1),
                "best_cost": float(global_cost),
            }
        )
        print(
            f"[PSO] iteration {iteration + 1:03d}/{iteration_count:03d} "
            f"best_cost={global_cost:.6g}"
        )

    return decode_parameters(global_position), float(global_cost), history


