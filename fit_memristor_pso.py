"""
fit_memristor_pso.py
====================
Guided particle-swarm parameter extraction for a bipolar memristive-device
compact model simulated with NGSpice.

The script performs two stages:

1. Coarse fitting on a reduced set of measurement points.
2. Refined fitting on the complete measured I-V trajectory.

The fitted SPICE template and best parameter table are written to
``results/fit/``. The two analysis programs in this repository read those
outputs directly.
"""

