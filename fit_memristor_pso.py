def main() -> None:
    ensure_directories()

    if not NGSPICE.exists():
        raise FileNotFoundError(
            "NGSpice was not found. Install it so the 'ngspice' command is "
            "available, or place ngspice.exe in the repository root."
        )

    data_path = select_measurement_path()
    voltage_full, current_full = read_measurement(data_path)
    icomp = estimate_positive_compliance(voltage_full, current_full)
    export_run_configuration(data_path, icomp)

    voltage_coarse, current_coarse = downsample_trajectory(
        voltage_full,
        current_full,
        COARSE_POINT_TARGET,
    )

    rng = np.random.default_rng(RANDOM_SEED)

    print("\n[STAGE 1] Coarse PSO")
    coarse_objective = make_objective(
        voltage_coarse,
        current_coarse,
        icomp,
        stage="coarse",
    )
    theta_coarse, cost_coarse, history_coarse = run_pso(
        objective=coarse_objective,
        particle_count=N_PARTICLES_COARSE,
        iteration_count=N_ITERATIONS_COARSE,
        rng=rng,
    )

    print("\n[STAGE 2] Refined PSO")
    refine_objective = make_objective(
        voltage_full,
        current_full,
        icomp,
        stage="refine",
    )
    theta_best, cost_best, history_refine = run_pso(
        objective=refine_objective,
        particle_count=N_PARTICLES_REFINE,
        iteration_count=N_ITERATIONS_REFINE,
        rng=rng,
        initial_center=theta_coarse,
    )

    export_theta(theta_best)
    pd.DataFrame(history_coarse).to_csv(
        OUT_DIR / "pso_history_coarse.csv", index=False
    )
    pd.DataFrame(history_refine).to_csv(
        OUT_DIR / "pso_history_refine.csv", index=False
    )

    final_result = run_simulation(
        theta_best,
        voltage_full,
        icomp,
        tag="best",
    )
    if final_result is None:
        raise RuntimeError("The final NGSpice simulation failed.")

    final_frame = pd.DataFrame(
        {
            "time": final_result.time,
            "Vcmd": final_result.vcmd,
            "Vp": final_result.vp,
            "I": final_result.current,
            "x": final_result.x,
            "xh": final_result.xh,
        }
    )
    final_frame.to_csv(OUT_DIR / "best_simulation.csv", index=False)
    save_fit_plots(voltage_full, current_full, final_result)

    summary = pd.DataFrame(
        [
            {
                "stage": "coarse",
                "best_cost": cost_coarse,
            },
            {
                "stage": "refine",
                "best_cost": cost_best,
            },
        ]
    )
    summary.to_csv(OUT_DIR / "fit_summary.csv", index=False)

    print("\n[COMPLETE]")
